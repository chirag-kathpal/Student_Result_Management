    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String rollno=jTextField1.getText();
        String physics=jTextField2.getText();
        String chemistry=jTextField3.getText();
        String maths=jTextField4.getText();
        String english=jTextField5.getText();
        String dbms=jTextField6.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Asdf@1234");
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result (rollno,physics,chemistry,maths,english,dbms) values('"+rollno+"','"+physics+"','"+chemistry+"','"+maths+"','"+english+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null,"Successfully added the data.");
                setVisible(false);
                new insertnewresult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"This roll no doesn't exists.");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

